package com.example.oopandroidapi;

public class DrivingTestData {
    public int ageGroup;
    public int number;

    public DrivingTestData(int ageGroup,int number){
        this.ageGroup = ageGroup;
        this.number = number;
    }
    public int getAgeGroup(){
        return ageGroup;
    }
    public int getNumber(){
        return number;
    }

}
